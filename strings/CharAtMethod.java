package strings;

public class CharAtMethod {

	public static void main(String[] args) {

		String s = "Hello World!!";
		String S = "Hello\nWorld";
		System.out.println("After implementing the CharAt method ,  the first String is : "+s.charAt(0));
		System.out.println("After implementing the CharAt method ,  the second String is : "+s.charAt(3));
		System.out.println("After implementing the CharAt method ,  the third String is : "+S.charAt(5));
		System.out.println("After implementing the CharAt method ,  the fourth String is : "+s.charAt(7));
	}

}
