//EndWith() Method//

package strings;

public class EndsWithMethod {

	public static void main(String[] args) {

			String str = "Java Programming";
		    System.out.println(str.endsWith("mming")); 
		    System.out.println(str.endsWith("g")); 
		    System.out.println(str.endsWith("a Programming"));
		    System.out.println(str.endsWith("programming")); 
		    System.out.println(str.endsWith("Java")); 

		
	}

}
